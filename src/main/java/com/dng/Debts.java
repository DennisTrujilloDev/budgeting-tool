package com.dng;

public class Debts {

  int carPayment;
  int medicalBill;
  int studentLoan;
  int creditCardPayment;



  //Accessor methods
  public int getCarPayment() {
    return carPayment;
  }

  public void setCarPayment(int carPayment) {
    this.carPayment = carPayment;
  }

  public int getMedicalBill() {
    return medicalBill;
  }

  public void setMedicalBill(int medicalBill) {
    this.medicalBill = medicalBill;
  }

  public int getStudentLoan() {
    return studentLoan;
  }

  public void setStudentLoan(int studentLoan) {
    this.studentLoan = studentLoan;
  }

  public int getCreditCardPayment() {
    return creditCardPayment;
  }

  public void setCreditCardPayment(int creditCardPayment) {
    this.creditCardPayment = creditCardPayment;
  }
}
